package com.qurion.businesslogic.hr.model;

import java.util.Date;

import javax.persistence.Entity;
import javax.persistence.Column;
import javax.persistence.FetchType;
import javax.persistence.JoinColumn;
import javax.persistence.ManyToOne;
import javax.persistence.OneToMany;
import javax.persistence.Table;
import javax.persistence.UniqueConstraint;

import org.codehaus.jackson.annotate.JsonIgnore;
import org.codehaus.jackson.annotate.JsonIgnoreProperties;
import org.codehaus.jackson.map.annotate.JsonSerialize;

import com.qurion.businesslogic.application.model.BaseEntity;
import com.qurion.businesslogic.hr.model.TimeSheetRoleType;
import com.qurion.businesslogic.party.model.Party;
import com.qurion.businesslogic.hr.model.TimeSheet;

import java.util.Date;
import java.util.Date;

/**
 * TimeSheetRole 
 * @author Edward Banfa
 */
@Entity
@Table(name="TIME_SHEET_ROLE"
    ,catalog="businesslogic"
    ,uniqueConstraints = @UniqueConstraint(columnNames="CODE") 
)
@JsonIgnoreProperties({"hibernateLazyInitializer", "handler"})
@JsonSerialize(include=JsonSerialize.Inclusion.NON_NULL)
public class TimeSheetRole  extends BaseEntity implements java.io.Serializable {
	private TimeSheetRoleType timeSheetRoleType;
	private Party party;
	private TimeSheet timeSheet;
	private Date fromDt;
	private Date toDt;

    public TimeSheetRole() {
    }

    public TimeSheetRole(Date fromDt, Integer id, String code, Date effectiveDt, Character recSt, Date createdDt, String createdByUsr) 
    {
        this.fromDt = fromDt;
        this.id = id;
        this.code = code;
        this.effectiveDt = effectiveDt;
        this.recSt = recSt;
        this.createdDt = createdDt;
        this.createdByUsr = createdByUsr;
    }

    public TimeSheetRole(TimeSheetRoleType timeSheetRoleType, Party party, TimeSheet timeSheet, Date fromDt, Date toDt, Integer id, String code, Date effectiveDt, Character recSt, Integer versionNo, Date rowTs, Date createdDt, String createdByUsr, Date lastModifiedDt, String lastModifiedUsr) 
    {
        this.timeSheetRoleType = timeSheetRoleType;
        this.party = party;
        this.timeSheet = timeSheet;
        this.fromDt = fromDt;
        this.toDt = toDt;
        this.id = id;
        this.code = code;
        this.effectiveDt = effectiveDt;
        this.recSt = recSt;
        this.versionNo = versionNo;
        this.rowTs = rowTs;
        this.createdDt = createdDt;
        this.createdByUsr = createdByUsr;
        this.lastModifiedDt = lastModifiedDt;
        this.lastModifiedUsr = lastModifiedUsr;
    }
    
    @ManyToOne(fetch=FetchType.LAZY)
    @JoinColumn(name="ROLE_TY_ID")
    @JsonIgnore
    public TimeSheetRoleType getTimeSheetRoleType() 
    {
        return this.timeSheetRoleType;
    }
    
    public void setTimeSheetRoleType(TimeSheetRoleType timeSheetRoleType)
    {
        this.timeSheetRoleType = timeSheetRoleType;
    }
    
    @ManyToOne(fetch=FetchType.LAZY)
    @JoinColumn(name="PARTY_ID")
    @JsonIgnore
    public Party getParty() 
    {
        return this.party;
    }
    
    public void setParty(Party party)
    {
        this.party = party;
    }
    
    @ManyToOne(fetch=FetchType.LAZY)
    @JoinColumn(name="TIME_SHEET_ID")
    @JsonIgnore
    public TimeSheet getTimeSheet() 
    {
        return this.timeSheet;
    }
    
    public void setTimeSheet(TimeSheet timeSheet)
    {
        this.timeSheet = timeSheet;
    }
    
    @Column(name="FROM_DT", nullable=false, length=10)
    public Date getFromDt() 
    {
        return this.fromDt;
    }
    
    public void setFromDt(Date fromDt) 
    {
        this.fromDt = fromDt;
    }
	
    @Column(name="TO_DT", length=10)
    public Date getToDt() 
    {
        return this.toDt;
    }
    
    public void setToDt(Date toDt) 
    {
        this.toDt = toDt;
    }
	
    

}
